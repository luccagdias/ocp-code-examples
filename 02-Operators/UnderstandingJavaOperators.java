/* --- UNDERSTANDING JAVA OPERATORS --- */

/* Terminology
 *      - Operator = special symbol that can be applied to a set of variables, values or literals
 *                   and that returns a result.
 *
 *      - Operand  = the value or variable the operator is being applied to.
 *
 */


/* Types of Operators 
 *      - Unary, Binary or Ternary: can be applied to one, two or three operands, respectively.
 *
 */


/* Operator Precedence
 *      - Unless overridden with parentheses, Java oeprators follow order of operation, listed 
 *      below, by decreasing order of operator.
 *
 *      - If two operators hava the same level of precedence, then Java guarantees left-to-right
 *      evaluation for most operators.
 *
 *      Order of operator precedence:
 *          - Post-unary operators
 *          - Pre-unary operators
 *          - Other unary operators
 *          - Cast
 *          - Mutiplication/division/modulus
 *          - Addiction/subtraction
 *          - Shift operators
 *          - Relational operators
 *          - Equal to/not equal to
 *          - Logical AND
 *          - Logical exlusive OR
 *          - Logical inclusive OR
 *          - Conditional AND
 *          - Conditional OR
 *          - Ternary operators
 *          - Assignment operators
 *          - Arrow operator
 *
 */
